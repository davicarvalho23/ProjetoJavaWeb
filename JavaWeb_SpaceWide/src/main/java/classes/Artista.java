package classes;

public class Artista {
private int id; 
private String nome; 
private String nome_artistico;
private String email;
private String senha; 
private String genero; 
private long biografia;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public String getNome_artistico() {
	return nome_artistico;
}
public void setNome_artistico(String nome_artistico) {
	this.nome_artistico = nome_artistico;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getSenha() {
	return senha;
}
public void setSenha(String senha) {
	this.senha = senha;
}
public String getGenero() {
	return genero;
}
public void setGenero(String genero) {
	this.genero = genero;
}
public long getBiografia() {
	return biografia;
}
public void setBiografia(long biografia) {
	this.biografia = biografia;
}




}
